CODE			EQU	$100	NORMAL DEFAULT

SYSCALL			EQU	$00FB
SYSCALL:WRITEA		EQU	$0C
WRITEA:SCLEN		EQU	$08
SCBLK:WLEN		EQU	$02
SYSCALL:EXIT		equ	$11

		ORG	CODE

		LDX	#MSG
		JSR	SYSCALL
		ldx	#exit
		JSR	SYSCALL			return to sdos
	
MSG		FCB	SYSCALL:WRITEA		FUNCTION
		FCB	WRITEA:SCLEN
		FCB	0			CHANNEL
		RMB	1			ANY GARBAGE
		FDB	MSGTXT			BUFFER ADDRESS
		FDB	MSGEND-MSGTXT		REQUESTED COUNT

MSGTXT		FCC	'Hola'
		FCB	13
MSGEND		EQU	*

exit		fcb	SYSCALL:EXIT
		fcb	SCBLK:WLEN

		END	CODE
		
		
